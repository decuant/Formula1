	/// <summary>
	/// 
	/// </summary>
	public sealed class clsTokenizer : StringCollection
	{
		private string m_szOriginal = "";

		//-----------------------------------------------------------------------------
		//-----------------------------------------------------------------------------
		/// <summary>
		/// 
		/// </summary>
		public clsTokenizer()
		{
		}

		//-----------------------------------------------------------------------------
		//-----------------------------------------------------------------------------
		/// <summary>
		/// 
		/// </summary>
		/// <param name="szInputString"></param>
		public void Assign(string szInputString)
		{
			//	remove any token from previous build
			//
			this.Clear();

			if (null == szInputString)
			{
				return;
			}

			//	assign the new string
			//
			m_szOriginal = szInputString.Trim();

			//	split into tokens
			//
			string[] vTokens = m_szOriginal.Split(';');

			foreach (string szWord in vTokens)
			{
				if (0 < szWord.Length)
				{
					this.Add(szWord);
				}
			}
		}

	}

	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
	/// <summary>
	/// 
	/// </summary>
	public sealed class clsTotals
	{
		public int iDirs;
		public int iFiles;
		public int iGreps;
		public DateTime dtStartTime;
		public DateTime dtStopTime;

		//-----------------------------------------------------------------------------
		//-----------------------------------------------------------------------------
		/// <summary>
		/// 
		/// </summary>
		public clsTotals()
		{
			Reset();
		}

		//-----------------------------------------------------------------------------
		//-----------------------------------------------------------------------------
		/// <summary>
		/// 
		/// </summary>
		public void Reset()
		{
			iDirs = 0;
			iFiles = 0;
			iGreps = 0;
			dtStartTime = System.DateTime.Now;
			dtStopTime = System.DateTime.Now;
		}
	}

